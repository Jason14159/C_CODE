#include <stdio.h>

void main() {

    char ch = 'A';
    char str[20] = "www.iBot.nz";
    float flt = 10.234;
    int no = 150;
    double dbl = 20.123456;
    printf("Character %c \n", ch);
    printf("String %s \n", str);
    printf("Float %f \n", flt);
    printf("Integer %d \n", no);
    printf("Double float %lf \n", dbl);
    printf("Octal %o \n", no);
    printf("Hexidecimal %x \n", no);
}